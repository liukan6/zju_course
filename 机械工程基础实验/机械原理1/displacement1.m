% 定义凸轮转角（单位：度）和对应的直动从动件位移（单位：毫米）
angles = 0:10:350; % 每隔10°的数据
displacement = [14.465, 16.238, 17.925, 19.386, 20.680, 21.675, 22.305, ...
                22.603, 22.505, 22.050, 21.246, 20.165, 18.824, 17.306, ...
                15.632, 13.908, 12.110, 10.435, 8.800, 7.350, 5.917, ...
                4.821, 3.840, 3.085, 2.520, 2.241, 2.220, 2.440, 2.943, ...
                3.721, 4.723, 6.000, 7.390, 9.031, 10.713, 12.478];

% 绘制位移图
figure;
plot(angles, displacement, '-o', 'LineWidth', 1.5, 'MarkerSize', 6);
grid on;

% 设置图形标题和坐标轴标签
title('滚子从动件位移图');
xlabel('凸轮转角（度）');
ylabel('从动件位移（mm）');
grid on;
% 设置坐标轴范围
xlim([0 360]);
ylim([0 max(displacement) + 1]);

